#include <iostream>

int main() {
    int myInteger = 10;
    std::cout << "Integer value: " << myInteger << std::endl;
    float myFloat = 3.14;
    std::cout << "Float value: " << myFloat << std::endl;
    char myCharacter = 'A';
    std::cout << "Character value: " << myCharacter << std::endl;

    return 0;
}
